package kg.gov.mf.loan.web.controller.doc.dto;

public class SearchResult {

    private Long id;
    private String text;

    public SearchResult(Long id, String text) {
        this.id = id;
        this.text = text;
    }

    public Long getId() {
        return id;
    }

    public void setId(Long id) {
        this.id = id;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }
}
